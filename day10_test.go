func TestReadMapToPoint(t *testing.T) {
	tests := []struct {
		input          string
		expectedOutput []Point
	}{
		{`..
.#`, []Point{{1, 1}}},
		{`#.#
.#.
...`, []Point{{0, 0}, {2, 0}, {1, 1}}},
	}
	for _, tt := range tests {
		out := readMapToPoint(tt.input)
		assert.EqualValues(t, tt.expectedOutput, out)
	}
}

